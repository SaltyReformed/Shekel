class TestEntrypointLoaderSourceShape:
    """The entrypoint carries the _load_secret helper with the right contract.

    Source-level checks pair with the behavioural tests below: a
    regression that removes the helper or changes its return-shape
    would fail BOTH this test class (immediately) and the behavioural
    class (slowly, via shell exec).  Catching it at source level
    surfaces the breakage without spawning bash.
    """

    @pytest.fixture(scope="class")
    def entrypoint_text(self) -> str:
        """Return the full entrypoint.sh source as a string."""
        return ENTRYPOINT_SCRIPT.read_text(encoding="utf-8")

    def test_loader_function_is_defined(self, entrypoint_text: str) -> None:
        """``_load_secret() {`` appears in entrypoint.sh."""
        assert re.search(
            r"^_load_secret\(\)\s*\{",
            entrypoint_text,
            re.MULTILINE,
        ), (
            "entrypoint.sh no longer defines _load_secret; the F-148 "
            "Docker secrets loader has been removed (Commit C-38 regression)."
        )

    @pytest.mark.parametrize(
        "var_name,file_name",
        [
            ("SECRET_KEY", "secret_key"),
            ("POSTGRES_PASSWORD", "postgres_password"),
            ("APP_ROLE_PASSWORD", "app_role_password"),
            ("TOTP_ENCRYPTION_KEY", "totp_encryption_key"),
            ("TOTP_ENCRYPTION_KEY_OLD", "totp_encryption_key_old"),
        ],
    )
    def test_loader_is_invoked_for_each_secret(
        self, entrypoint_text: str, var_name: str, file_name: str,
    ) -> None:
        """Every documented secret has a ``_load_secret <VAR> <file>`` call."""
        # Match the canonical invocation.  Whitespace tolerant but
        # we require the exact var/file pair so a typo
        # (``_load_secret SECRET_KEY postgres_password``) would
        # surface here instead of as a runtime auth error.
        assert re.search(
            rf"^\s*_load_secret\s+{var_name}\s+{file_name}\b",
            entrypoint_text,
            re.MULTILINE,
        ), (
            f"entrypoint.sh does not invoke ``_load_secret {var_name} "
            f"{file_name}``; the secret will not be loaded from the "
            "file-backed posture and the operator's docker secret "
            "would silently fall back to the env value (F-148 regression)."
        )

    def test_loader_invocations_precede_secret_key_validation(
        self, entrypoint_text: str,
    ) -> None:
        """Every ``_load_secret`` call appears BEFORE the SECRET_KEY shape check.

        Ordering invariant: the SECRET_KEY length / placeholder check
        only works correctly if the env var is already populated from
        whichever source (file or env).  A regression that runs the
        loader AFTER the validation would let a placeholder pass the
        shape check and then have its real value loaded -- the wrong
        value would never be inspected, defeating the validation.
        """
        loader_calls = list(re.finditer(
            r"^\s*_load_secret\s+\w+\s+\w+\b",
            entrypoint_text,
            re.MULTILINE,
        ))
        assert loader_calls, "no _load_secret invocations found"
        # The validation block opens with a ``-z "${SECRET_KEY}"``
        # check.  Find the earliest one.
        validation_match = re.search(
            r'\[\s*-z\s+"\$\{SECRET_KEY\}"\s*\]',
            entrypoint_text,
        )
        assert validation_match is not None, (
            "entrypoint.sh no longer validates SECRET_KEY shape"
        )
        last_loader_offset = max(m.start() for m in loader_calls)
        assert last_loader_offset < validation_match.start(), (
            "_load_secret invocation appears AFTER the SECRET_KEY "
            "shape check; the validation runs against the env-only "
            "value and the file-backed override would slip past it."
        )

    def test_secret_key_placeholder_rejected_at_entrypoint(
        self, entrypoint_text: str,
    ) -> None:
        """``replaced_by_docker_secret*`` rejected by the SECRET_KEY case statement.

        The placeholder is the documented form an operator leaves in
        .env when migrating to Posture 2 (file-backed secrets).  If
        the secret file at /run/secrets/secret_key is missing or
        unreadable, the placeholder remains as the active SECRET_KEY
        -- a publicly-known value.  Catching it here closes the gap
        where the operator believes they are on Posture 2 but is
        actually running on the placeholder.
        """
        # The case statement must include ``replaced_by_docker_secret*``
        # as one of the rejected patterns alongside the historical
        # placeholders.
        assert re.search(
            r"replaced_by_docker_secret\*",
            entrypoint_text,
        ), (
            "entrypoint.sh case statement does not reject the "
            "``replaced_by_docker_secret*`` placeholder; an operator "
            "whose docker secret file is missing would silently boot "
            "under the public placeholder value (audit finding F-148 "
            "regression / hole in Commit C-38)."
        )

    def test_loader_rebuilds_database_url_when_postgres_secret_loaded(
        self, entrypoint_text: str,
    ) -> None:
        """The DATABASE_URL rebuild branch fires when POSTGRES_PASSWORD comes from a file.

        Compose interpolates ``DATABASE_URL`` from
        ``${POSTGRES_PASSWORD}`` at config-time, baking whatever
        placeholder the operator put in .env into the URI.  When the
        loader overwrites ``POSTGRES_PASSWORD`` with the real value
        from /run/secrets, ``DATABASE_URL`` goes stale and the deploy
        scripts (init_database.py, seed_*.py) connect under the
        placeholder -- which the postgres role does not recognise.
        Asserting on the rebuild branch catches a regression that
        removes it.
        """
        # The rebuild block is conditional on the
        # ``_postgres_password_loaded_from_file`` flag.  Match the
        # opening conditional.
        assert re.search(
            r'_postgres_password_loaded_from_file.*?-eq\s+1.*?\bexport\s+DATABASE_URL=',
            entrypoint_text,
            re.DOTALL,
        ), (
            "entrypoint.sh no longer rebuilds DATABASE_URL when "
            "POSTGRES_PASSWORD comes from a docker secret file; the "
            "deploy scripts will connect under the .env placeholder "
            "instead of the real password."
        )
        # And DB_PASSWORD must be rebuilt too -- psql calls in the
        # entrypoint use it directly.
        assert re.search(
            r'_postgres_password_loaded_from_file.*?-eq\s+1.*?\bexport\s+DB_PASSWORD=',
            entrypoint_text,
            re.DOTALL,
        ), (
            "entrypoint.sh no longer rebuilds DB_PASSWORD when "
            "POSTGRES_PASSWORD comes from a docker secret file; the "
            "psql schema-creation and role-provisioning calls will "
            "use the .env placeholder password instead."
        )


