def _extract_loader_function(test_secrets_dir: Path) -> str:
    """Return the bash source of ``_load_secret`` rewritten to look in
    ``test_secrets_dir`` instead of /run/secrets.

    Extracts the function from the live entrypoint.sh so the
    behavioural tests exercise the production code (not a
    duplicate).  The single string substitution ``"/run/secrets/"`` ->
    ``"<test_dir>/"`` lets the otherwise-unmodified function read
    from the test fixture.

    The substitution ONLY targets the literal path inside the
    ``secret_path`` assignment.  The error-message references to
    ``/opt/docker/shekel/secrets/`` are operator hints and are not
    rewritten -- their content is irrelevant to behaviour and a
    rewrite would obscure the test output if a failure surfaces.
    """
    text = ENTRYPOINT_SCRIPT.read_text(encoding="utf-8")
    # Capture the function from "_load_secret() {" to the matching
    # "}" at column 0.  ``re.MULTILINE`` so ``^`` matches the start
    # of any line; ``re.DOTALL`` so ``.`` crosses newlines.
    match = re.search(
        r"^(_load_secret\(\)\s*\{.*?\n^\})",
        text,
        re.MULTILINE | re.DOTALL,
    )
    if match is None:
        pytest.fail(
            "Could not extract _load_secret from entrypoint.sh; the "
            "function must start with ``_load_secret() {`` at column "
            "0 and end with ``}`` at column 0."
        )
    func_source = match.group(1)
    # Rewrite only the literal path inside the secret_path
    # assignment.  The function uses
    # ``local secret_path="/run/secrets/${file_name}"`` -- we replace
    # ``/run/secrets/`` with the absolute test path.
    rewritten = func_source.replace("/run/secrets/", f"{test_secrets_dir}/")
    return rewritten


def _run_loader_script(loader_func_source: str, body: str) -> subprocess.CompletedProcess[str]:
    """Run a bash script that sources the loader and executes ``body``."""
    script = (
        "#!/bin/bash\n"
        "set -eEo pipefail\n"
        f"{loader_func_source}\n\n"
        f"{body}\n"
    )
    return subprocess.run(
        ["bash", "-c", script],
        capture_output=True,
        text=True,
        timeout=BASH_TEST_TIMEOUT_S,
        check=False,
    )


class TestEntrypointLoaderBehaviour:
    """Run the extracted ``_load_secret`` against real fixture files.

    These tests prove the function actually:
    1. Loads a value when the file exists.
    2. Falls back silently (env unchanged) when the file is absent.
    3. Rejects an empty file with a clear diagnostic.
    4. Strips trailing newlines (``echo`` writes them) without
       mangling internal newlines or special characters.
    """

    def test_loader_reads_secret_when_file_present(self, tmp_path: Path) -> None:
        """A populated file overrides any env value the helper sees."""
        secrets_dir = tmp_path / "secrets"
        secrets_dir.mkdir()
        (secrets_dir / "secret_key").write_text("a" * SECRET_KEY_HEX_BYTES)
        loader = _extract_loader_function(secrets_dir)

        result = _run_loader_script(
            loader,
            (
                # Pre-set the env var to a known wrong value so we
                # can prove the loader OVERWROTE it (not merely
                # appended to a missing var).
                "export TEST_SECRET_KEY=stale-env-value\n"
                "_load_secret TEST_SECRET_KEY secret_key\n"
                "echo \"VAR=${TEST_SECRET_KEY}\""
            ),
        )

        assert result.returncode == 0, (
            f"loader exited non-zero: stderr={result.stderr!r}"
        )
        # Exact match: 64 ``a`` characters, no trailing whitespace.
        assert f"VAR={'a' * SECRET_KEY_HEX_BYTES}\n" in result.stdout, (
            f"loader did not overwrite env var with file content; "
            f"stdout={result.stdout!r}"
        )

    def test_loader_falls_back_to_env_when_file_absent(self, tmp_path: Path) -> None:
        """A missing file is a silent no-op; env value is preserved."""
        secrets_dir = tmp_path / "secrets"
        secrets_dir.mkdir()
        # Note: NO file created.  The loader should not error.
        loader = _extract_loader_function(secrets_dir)

        result = _run_loader_script(
            loader,
            (
                "export TEST_SECRET=fallback-env-value\n"
                # The argument 'nonexistent_file' has no matching
                # file in secrets_dir, so the loader returns 0
                # without touching the var.
                "_load_secret TEST_SECRET nonexistent_file\n"
                "echo \"VAR=${TEST_SECRET}\""
            ),
        )

        assert result.returncode == 0, (
            f"loader exited non-zero on missing file: stderr={result.stderr!r}"
        )
        assert "VAR=fallback-env-value" in result.stdout, (
            "loader mutated the env var when the secret file was "
            f"absent; stdout={result.stdout!r}"
        )
        # No "Loaded ..." log line should fire when the file is absent.
        assert "Loaded " not in result.stdout, (
            "loader logged a ``Loaded ...`` line for a missing file; "
            "the no-op branch should be silent."
        )

    def test_loader_rejects_empty_secret_file(self, tmp_path: Path) -> None:
        """A present-but-empty file is a misconfiguration -- fail fast."""
        secrets_dir = tmp_path / "secrets"
        secrets_dir.mkdir()
        # Touch the file so it exists but contains no bytes.
        (secrets_dir / "empty_file").write_bytes(b"")
        loader = _extract_loader_function(secrets_dir)

        result = _run_loader_script(
            loader,
            "_load_secret TEST_SECRET empty_file",
        )

        assert result.returncode != 0, (
            "loader accepted an empty secret file and would have "
            "exported an empty value -- the operator's intent to use "
            "file-backed secrets is clear (file exists) but the file "
            "is empty; this is a misconfiguration that must fail fast."
        )
        assert "is present but empty" in result.stderr, (
            f"loader did not emit the expected diagnostic for an empty "
            f"secret file; stderr={result.stderr!r}"
        )

    def test_loader_strips_trailing_newline(self, tmp_path: Path) -> None:
        """``echo "value" > file`` writes a trailing newline; loader strips it.

        Bash's ``$(< file)`` command substitution strips trailing
        newlines, so a file written by the typical ``echo`` pattern
        produces the same in-process value as ``printf 'value' >
        file``.  This is documented in the helper's source comment
        and is the load-bearing reason the helper uses ``$(< ...)``
        instead of ``cat``.
        """
        secrets_dir = tmp_path / "secrets"
        secrets_dir.mkdir()
        # Use ``write_text`` -- Python's text-mode write does NOT add
        # a trailing newline, so we add one explicitly to mirror the
        # ``echo "value" > file`` shell pattern.
        (secrets_dir / "with_newline").write_text("value-with-trailing-newline\n")
        loader = _extract_loader_function(secrets_dir)

        result = _run_loader_script(
            loader,
            (
                "_load_secret TEST_VAR with_newline\n"
                # ``${#TEST_VAR}`` is the byte length.  29 chars
                # without the trailing newline; 30 with it.  We
                # assert on the length directly so a regression that
                # leaves the newline in surfaces as a precise number,
                # not a fuzzy "not equal".
                "echo \"LEN=${#TEST_VAR}\"\n"
                "echo \"VAL=${TEST_VAR}\""
            ),
        )

        assert result.returncode == 0, result.stderr
        # 27 = len("value-with-trailing-newline").
        assert "LEN=27" in result.stdout, (
            f"loader did not strip the trailing newline; stdout={result.stdout!r}"
        )
        assert "VAL=value-with-trailing-newline" in result.stdout

    def test_entrypoint_rejects_placeholder_when_secret_file_missing(
        self,
    ) -> None:
        """The entrypoint case statement rejects ``replaced_by_docker_secret*``.

        Behavioural check: extract the SECRET_KEY validation block
        from entrypoint.sh, run it in bash with the placeholder set
        as SECRET_KEY, assert the script exits non-zero with the
        placeholder diagnostic.  Mirrors the source-shape assertion
        in TestEntrypointLoaderSourceShape so a regression that
        leaves the case statement intact but breaks the case match
        (e.g. typo in the pattern) is still caught.
        """
        # Extract the entire SECRET_KEY validation section (header
        # ``# ── 0a.``) up to the next section (``# ── 0b.``).  The
        # placeholder (32+ chars) clears the length check, so the
        # case statement is what must catch it.  We capture the
        # whole 0a section because the case statement now contains
        # a nested case for the docker-secret hint -- a regex that
        # tries to capture only up to the first ``esac`` would
        # truncate at the inner one.
        text = ENTRYPOINT_SCRIPT.read_text(encoding="utf-8")
        match = re.search(
            r"(# ── 0a\. Validate SECRET_KEY shape.*?)(?=\n# ── 0b\.)",
            text,
            re.DOTALL,
        )
        if match is None:
            pytest.fail(
                "Could not extract SECRET_KEY validation section "
                "from entrypoint.sh; the section must start with "
                "``# ── 0a. Validate SECRET_KEY shape`` and end "
                "before ``# ── 0b.``."
            )
        validation = match.group(1)
        script = (
            "#!/bin/bash\n"
            "set -eEo pipefail\n"
            # 32+ char placeholder so the length check passes.
            'export SECRET_KEY="replaced_by_docker_secret_padded_to_be_long_enough"\n'
            f"{validation}\n"
            'echo "should-not-reach"\n'
        )
        result = subprocess.run(
            ["bash", "-c", script],
            capture_output=True,
            text=True,
            timeout=BASH_TEST_TIMEOUT_S,
            check=False,
        )
        assert result.returncode != 0, (
            "entrypoint.sh validation accepted "
            "``replaced_by_docker_secret_padded_...`` as a valid "
            "SECRET_KEY; the placeholder pattern is not in the "
            "rejected case branches."
        )
        assert "known placeholder" in result.stderr, (
            f"entrypoint.sh did not emit the ``known placeholder`` "
            f"diagnostic for the docker-secret placeholder; "
            f"stderr={result.stderr!r}"
        )
        # The hint about the missing /run/secrets/secret_key file
        # must surface so the operator knows where to look.
        assert "Migrating to Docker secrets" in result.stderr, (
            "entrypoint.sh placeholder rejection does not point the "
            "operator at the runbook section; the operator has no "
            "discoverable hint that the secret file is missing."
        )
        # ``should-not-reach`` confirms exit fired.
        assert "should-not-reach" not in result.stdout

    def test_loader_preserves_special_characters(self, tmp_path: Path) -> None:
        """Special characters (URL-safe base64, punctuation) survive the load.

        Fernet keys are URL-safe base64 (``A-Za-z0-9_-=``).  TOTP
        encryption keys generated by ``Fernet.generate_key()`` end
        with ``=`` padding which a sloppy shell substitution
        (``eval``, double-quoted ``$VAR`` in the wrong context) could
        mangle.  Random bash special chars (``$``, `` ``, ``"``)
        are tested so a future use of ``$()`` or unquoted variable
        expansion in the helper surfaces here.
        """
        secrets_dir = tmp_path / "secrets"
        secrets_dir.mkdir()
        # Use printf via Python directly to avoid any shell parsing.
        special = "k_AbC123-_=+/!@#$%^&*() with spaces and \"quotes\""
        (secrets_dir / "special_secret").write_text(special)
        loader = _extract_loader_function(secrets_dir)

        # We base64-encode the loaded value to assert on it without
        # going through any further shell quoting -- this isolates
        # the test from quoting bugs in the assertion harness itself.
        result = _run_loader_script(
            loader,
            (
                "_load_secret TEST_VAR special_secret\n"
                # ``printf '%s' "$TEST_VAR" | base64 -w0`` produces a
                # single-line base64 string we can compare exactly.
                "printf '%s' \"$TEST_VAR\" | base64 -w0\n"
                "echo \"\""  # newline so output is parseable
            ),
        )

        assert result.returncode == 0, result.stderr
        expected_b64 = base64.b64encode(special.encode("utf-8")).decode("ascii")
        assert expected_b64 in result.stdout, (
            f"loader mangled special characters; expected b64="
            f"{expected_b64!r}, got stdout={result.stdout!r}"
        )


