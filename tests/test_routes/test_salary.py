@pytest.fixture(autouse=True)
def _freeze_today_inside_seed_range(monkeypatch):
    """Freeze today to date(2026, 3, 20) so seed_periods tests pass past 2026-05-22.

    Salary tests rely on tax_year=2026 records, calendar 2026
    transactions generated by the recurrence engine, and seed_periods_52
    which spans 2026-2027 from a calendar anchor.  Migrating to a
    today-relative fixture would slide the period range out of the
    tax_year=2026 frame.  Freezing today inside the seeded range keeps
    get_current_period() deterministic regardless of wall-clock date.
    """
    freeze_today(monkeypatch, date(2026, 3, 20))


def _create_profile(seed_user):
    """Helper: create a salary profile with linked template and recurrence."""
    filing_status = db.session.query(FilingStatus).filter_by(name="single").one()
    income_type = db.session.query(TransactionType).filter_by(name="Income").one()
    every_period = db.session.query(RecurrencePattern).filter_by(name="Every Period").one()

    # Find or create Salary category.
    cat = (
        db.session.query(Category)
        .filter_by(user_id=seed_user["user"].id, group_name="Income", item_name="Salary")
        .first()
    )
    if not cat:
        cat = Category(user_id=seed_user["user"].id, group_name="Income", item_name="Salary")
        db.session.add(cat)
        db.session.flush()

    rule = RecurrenceRule(
        user_id=seed_user["user"].id,
        pattern_id=every_period.id,
    )
    db.session.add(rule)
    db.session.flush()

    template = TransactionTemplate(
        user_id=seed_user["user"].id,
        account_id=seed_user["account"].id,
        category_id=cat.id,
        recurrence_rule_id=rule.id,
        transaction_type_id=income_type.id,
        name="Day Job",
        default_amount=Decimal("75000.00") / 26,
        is_active=True,
    )
    db.session.add(template)
    db.session.flush()

    profile = SalaryProfile(
        user_id=seed_user["user"].id,
        scenario_id=seed_user["scenario"].id,
        template_id=template.id,
        filing_status_id=filing_status.id,
        name="Day Job",
        annual_salary=Decimal("75000.00"),
        state_code="NC",
        pay_periods_per_year=26,
    )
    db.session.add(profile)
    db.session.commit()
    return profile


